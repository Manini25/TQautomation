class Pattern7
{
    public static void main(String args[])
    {
        for(int i=1;i<=5;i++)
        {
            for(int j=1;j<=j;j++)
            {
                if((i+j)<=7)
                {
                    System.out.print(" ");
                }

                else{
                    System.out.print("*");
                }
            }
            System.out.println();
        }
    }
}