class Casting 
{
 public static void main(String[] args)
 {
     double d=130;
     System.out.println("d="+d);

     int i='g';
     System.out.println("i="+i);//ASCII value

     int j=(int)453.021;
     System.out.println("j="+j);

     char c=(char)110.55;
     System.out.println("c="+c);//ASCII value
 }   
}
